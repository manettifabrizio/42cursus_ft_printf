#include "../libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	char	*space;
	size_t	i;

	i = 0;
	if (!(space = malloc(sizeof(char) * (nmemb * size))))
		return (NULL);
	while (i < size * nmemb)
	{
		space[i] = 0;
		i++;
	}
	return (space);
}
